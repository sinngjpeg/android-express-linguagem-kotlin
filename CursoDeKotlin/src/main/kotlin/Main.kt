fun main() {
    //GLOSSARIO (var, val, fun, class, double, String, int, false, true)

    // NUMEROS GRANDES
    val numero: Long = 1_123_321_123
    println(numero)

    // OPERADORES LOGICOS
    var idade = 31

    // SOMA
    idade = idade + 10
    println(idade)

    // SUBTRACAO
    idade = idade - 10
    println(idade)

    // DIVISAO
    idade = idade / 10
    println(idade)

    //MULTIPLICACAO
    idade = idade * 10
    println(idade)

    //TEXTOS
    var textoIdade = "10"
    println(textoIdade + " minha idade")

    val booleana = true
    // true / false ( 1 - 0 )
    println(booleana)
    println(!booleana)


}